// 构建期生成 wait-page 的 base64 模块，供 bootPreview.ts 导入。
// 原因：关于→预览此前用 `?raw` 导入 wait-page 的 .html，但 Vite 生产构建会把 .html 当 HTML
// 入口拦截、`?raw` 静默失效（dev 正常、build 丢内容），导致打包后预览为空。该脚本把同一份
// base64 落盘为 src/lib/_waitingPages.generated.ts，与 index.html 启动页共用单一可信源。
// 必须在 `tsc` 之前运行（build 脚本是 `tsc && vite build`），故接入 predev / beforeBuildCommand。
import fs from 'node:fs';
import { resolve } from 'node:path';
const root = process.cwd();
const light = fs.readFileSync(resolve(root, 'wait-page/waiting-page-light.html'), 'utf-8');
const dark = fs.readFileSync(resolve(root, 'wait-page/waiting-page-dark.html'), 'utf-8');
const out = resolve(root, 'src/lib/_waitingPages.generated.ts');
fs.writeFileSync(
  out,
  `// AUTO-GENERATED by scripts/gen-waiting-pages.mjs — 请勿手动编辑；由 wait-page/*.html 构建期生成。\n` +
  `export const WAITING_LIGHT_B64 = ${JSON.stringify(Buffer.from(light).toString('base64'))};\n` +
  `export const WAITING_DARK_B64 = ${JSON.stringify(Buffer.from(dark).toString('base64'))};\n`
);
console.log('[gen-waiting-pages] wrote', out);
